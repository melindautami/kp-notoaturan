    <button class="accordion">Apa sih yang dimaksud dengan HAKI?</button>
    <div class="panel">
        <p>HAKI atau Hak atas Kekayaan Intelektual merupakan hak eksklusif yang dimiliki oleh perorangan, kelompok orang atau badan usaha untuk menggunakan dan mendapatkan manfaat ekonomis dari kekayaan intelektual yang dimiliki dan/atau diciptakan. Istilah HAKI merupakan terjemahan dari Intellectual Property Right (IPR), sebagaimana diatur dalam undang-undang No. 7 Tahun 1994 tentang pengesahan WTO (Agreement Establishing The World Trade Organization).</p>
    </div>

    <button class="accordion">Apa saja yang termasuk di dalam HAKI?</button>
    <div class="panel">
        <p>Yang termasuk dalam HAKI adalah Hak Cipta (copyright), Paten (Patent), Merek (Trademark), selain itu juga ada Rahasia Dagang, Desain Industri, Indikasi Geografis, Sirkuit Terpadu, dan Perlindungan Varietas Tanaman. </p>
    </div>

    <button class="accordion">Kalau Merek apalagi tuh pengertiannya? </button>
    <div class="panel">
        <p>Merek merupakan bentuk perlindungan HAKI yang paling dekat dengan kehidupan sehari-hari. Barang atau jasa apapun yang kita butuhkan, lebih sering disebut dengan nama dagangnya ketimbang nama generiknya. Sejak sebelum memulai aktivitas pagi hari, Anda sarapan Sari Roti ditemani secangkir Nescafe Classic sambil membaca Kompas Online di iPad, baru kemudian berangkat ke kantor naik Innova. Dalam 1 (satu) kalimat tersebut, sudah berapa merek yang Anda sebutkan? Pengertian Merek berdasarkan Undang-Undang No. 20 Tahun 2016 tentang Merek dan Indikasi Geografis (UU No. 20/2016) adalah tanda yang dapat ditampilkan secara grafis berupa gambar, logo, nama, kata, huruf, angka, susunan warna, dalam bentuk 2 dimensi atau 3 dimensi, suara, hologram, atau kombinasi dari 2 atau lebih unsur tersebut untuk membedakan barang dan/atau jasa yang diproduksi oleh orang atau badan hokum dalam kegiatan perdagangan barang dan/atau jasa. Merek atau biasa dikenal dengan istilah brand, adalah identitas dari sebuah produk barang atau jasa. Namun tidak hanya sebagai identitas semata, merek juga berperan penting mewakili reputasi tidak hanya produknya, namun juga penghasil dari produk barang/jasa yang dimaksud. Tak heran jika branding menjadi bagian yang sangat penting dalam pemasaran suatu produk/jasa. Hak atas Merek adalah hak eksklusif yang diberikan oleh negara kepada pemilik merek terdaftar selama jangka waktu tertentu untuk menggunakan sendiri merek tersebut atau memberikan izin kepada orang lain untuk menggunakannya sesuai dengan klasifikasi kelas dan jenis barang/jasa yang didaftarkan. Hal yang perlu dipahami adalah pendaftaran Merek menganut prinsip ?first to file? atau yang dikenal dengan ?siapa yang pertama mendaftarkan, maka ia yang mendapatkan Hak atas Merek?. Oleh karena itu, dianjurkan agar mendaftarkan nama merek yang Anda miliki sedini mungkin, untuk menghindari penolakan atau keberatan pihak lain di kemudian hari. </p>
    </div>

